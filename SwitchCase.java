package problems_set_1;

public class SwitchCase {
	public static void main(String[] args) {
		int tech = 1;

		 switch(tech){
		case 1:
		 {
		 System.out.println("PHP");
		 break;
		 }
		 case 2:{
		 System.out.println("Python");
		 break;
		 }
		 default:{
		 System.out.println("Not Listed..");
		 }
		 }
	}
}
